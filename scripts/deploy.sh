# ── print_summary ───────────────────────────────────────────────────────────

print_summary() {
    echo ""
    echo -e "${BOLD}════════════════════════════════════════════════════════${NC}"
    echo -e "${BOLD}  Corre Deployment Summary${NC}"
    echo -e "${BOLD}════════════════════════════════════════════════════════${NC}"
    echo ""
    echo -e "  Binary:        ${BINARY_PATH}"
    echo -e "  Config:        ${CORRE_CONFIG}"
    echo -e "  Data dir:      ${CORRE_DATA_DIR}"
    echo -e "  Env file:      ${CORRE_ENV_FILE}"
    echo ""

    if [[ "${DID_SYSTEMD}" == true ]]; then
        local status
        status="$(systemctl is-active "${CORRE_SERVICE}" 2>/dev/null || echo "unknown")"
        echo -e "  Service:       ${CORRE_SERVICE} (${status})"
        echo -e "  Logs:          journalctl -u ${CORRE_SERVICE} -f"
    else
        echo -e "  Service:       not configured (run manually with: ${BINARY_PATH} run)"
    fi

    if [[ "${DID_TAILSCALE}" == true && -n "${TAILSCALE_IP}" ]]; then
        echo -e "  Tailscale IP:  ${TAILSCALE_IP}"
        echo -e "  Remote URL:    http://${TAILSCALE_IP}:5510"
    fi

    echo ""
    echo -e "  Local URL:     http://127.0.0.1:5510"
    echo ""
    echo -e "${BOLD}════════════════════════════════════════════════════════${NC}"
}

# ── main ────────────────────────────────────────────────────────────────────

main() {
    echo ""
    echo -e "${BOLD}Corre Deploy${NC}"
    echo -e "Repository: ${CORRE_REPO_DIR}"
    echo ""

    # Validate binary exists
    if [[ ! -x "${BINARY_PATH}" ]]; then
        error "Binary not found at ${BINARY_PATH}"
        error "Build it first with: cargo build --release"
        error "Or set BINARY_PATH to point at an existing binary."
        exit 1
    fi
    ok "Binary found at ${BINARY_PATH}"

    # Validate config exists
    if [[ ! -f "${CORRE_CONFIG}" ]]; then
        error "Config not found at ${CORRE_CONFIG}"
        exit 1
    fi
    ok "Config found at ${CORRE_CONFIG}"

    install_system_deps
    verify_mcp_servers
    setup_env_file
    setup_systemd
    setup_tailscale
    setup_tor_placeholder
    print_summary
}

main "$@"
